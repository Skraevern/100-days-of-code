# Data Types

# Strings
print(type("Hello")) # <class 'str'>
print("Hello"[0]) # H   Subscripting
print("Hello"[4]) # o

# Integer
# Whole numbers
print(type(123)) # <class 'int'>
print(123 + 346) # 469
print(1000000 + 1000000) # 2000000
print(1_000_000 + 1_000_000) # 2000000

# Float
# Decimal
print(type(12.45)) # <class 'float'>
print(12.45) # 12.54

# Boolean
# True / False. Capital first letter
print(type(True)) # <class 'bool'>